
import Head from 'next/head'
import { useState } from 'react'
import styles from '../styles/Home.module.css'

export default function Home() {
  const [address, setAddress] = useState("")

  const getFunds = async () => {
    try {
      const response = await fetch('api/getGoerli', {
        method: 'POST',
        body: JSON.stringify({ address }),
        headers: {
          'Content-Type': 'application/json'
        }
      });
      if (!response.ok) { return alert('Something went wrong in response ! ') }
      alert("Transaction initiated succesfully!")

    } catch (err) {
      console.error(err)
      alert("Something went wrong in api! ")
    }
  }

  const getValidation = async () => {
    try {
      let response = await fetch('api/getNftValidation', { //validate wheter is holder of NFT
        method: 'POST',
        body: JSON.stringify({ address }),
        headers: {
          'Content-Type': 'application/json'
        }
      });
      if (!response.ok) { // end the function if fail to validate the ownership of NFT
        console.log(response)
        return alert('Validation fail, you are not the holder')
        //you may use const res = await response.json()
        //return alert(res.message)
      }
      response = await fetch('api/getThresholdValidation', { //validate wheter is holding too high fund 
        method: 'POST',
        body: JSON.stringify({ address }),
        headers: {
          'Content-Type': 'application/json'
        }
      });
      if (!response.ok) { // end the function if client hold too much fund
        console.log(response)
        return alert('Validation fail,you have enough fund')
        //you may use const res = await response.json()
        //return alert(res.message)
      }
      alert("Validation succesful!!")
      //you may use const res = await response.json()
      //return alert(res.message)
      getFunds()
    } catch (err) {
      console.error(err)
    }
  }

  return (
    <div className={styles.container}>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <div
        style={{
          height: "100vh",
          width: "100vw",
          display: "flex",
          alignItems: "center",
          justifyContent: "center",
        }}>
        <input type="text" value={address} onChange={(e) => setAddress(e.target.value)}
          placeholder="Enter your wallet address" />
        <button onClick={getValidation}>Click to get Test Goerli</button>
      </div>

    </div>
  )
}
